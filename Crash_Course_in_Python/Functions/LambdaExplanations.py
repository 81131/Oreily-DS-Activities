#Lambda explanation
print("Lambda Explanation:")
multipleTwice = lambda x: x*2
result = multipleTwice(5)  # result is 10
print("Result of multipleTwice(5):", result)

#Lambda: This is a function
#x: x is the argument
#x*2: x*2 is the expression that is returned


#Lambda with multiple arguments
print("\nLambda with multiple arguments:")
add = lambda x, y: x + y
sum_result = add(3, 5)  # sum_result is 8
print("Result of add(3, 5):", sum_result)